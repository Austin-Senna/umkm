import puppeteerDeploy from './src/api/puppeteer-deploy.js';

// Test HTML content
const testHtml = `
<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>Test Website</title>
    <style>
        body { font-family: Arial, sans-serif; margin: 40px; }
        .container { max-width: 800px; margin: 0 auto; }
        h1 { color: #333; }
        p { color: #666; line-height: 1.6; }
    </style>
</head>
<body>
    <div class="container">
        <h1>Test Website</h1>
        <p>This is a test website generated by the UMKM Go Digital platform.</p>
        <p>If you can see this, the deployment was successful!</p>
    </div>
</body>
</html>
`;

async function testPuppeteerDeployment() {
  console.log('Testing puppeteer deployment...');
  
  try {
    const result = await puppeteerDeploy(testHtml, 'test-website');
    
    console.log('Deployment result:', result);
    
    if (result.success) {
      console.log('✅ Puppeteer deployment test successful!');
      console.log(`🌐 Website URL: ${result.url}`);
      console.log(`📅 Deployed at: ${new Date(result.deployedAt).toISOString()}`);
    } else {
      console.log('❌ Puppeteer deployment test failed!');
      console.log(`Error: ${result.error}`);
    }
    
  } catch (error) {
    console.error('❌ Test failed with exception:', error);
  }
}

// Run the test
testPuppeteerDeployment(); 